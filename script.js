let currentArticle = 'xu';

function loadArticle(articleId) {
    const article = articles[articleId];
    if (!article) return;

    const titleEl = document.getElementById('article-title');
    const dateEl = document.querySelector('.article-meta');
    const bodyEl = document.getElementById('article-body');

    titleEl.textContent = article.title;
    dateEl.textContent = article.date;
    bodyEl.innerHTML = article.content;

    currentArticle = articleId;

    document.querySelectorAll('.article-item').forEach(item => {
        item.classList.remove('active');
        if (item.dataset.article === articleId) {
            item.classList.add('active');
        }
    });

    updatePagination();

    window.scrollTo(0, 0);
}

function updatePagination() {
    const article = articles[currentArticle];
    const prevEl = document.querySelector('.pagination-item.prev');
    const nextEl = document.querySelector('.pagination-item.next');

    if (article.prev && articles[article.prev]) {
        prevEl.style.display = 'flex';
        prevEl.querySelector('.pagination-title').textContent = articles[article.prev].title;
    } else {
        prevEl.style.display = 'none';
    }

    if (article.next && articles[article.next]) {
        nextEl.style.display = 'flex';
        nextEl.querySelector('.pagination-title').textContent = articles[article.next].title;
    } else {
        nextEl.style.display = 'none';
    }
}

